               <h3>About Insurance</h3>
               <p>CWith insurance, you choose what you want to be protected against.

Then your insurer calculates the risk that the events to be insured will happen and the insurance provider or insurer will determine the price you will need to pay (your premium).</p>
                
                <h4>These are the three main steps:</h4>

                <li>Choose a policy. An insurance policy is a document that lists exactly what you are or aren’t protected against. For example, a travel insurance policy might say that it’ll cover your medical bills if you injure yourself abroad – but not if you were doing something dangerous, like skiing.</li>
                <li>Pay the premium. The premium is the amount you pay each month or year (or sometimes just once) to have the insurance. The amount you pay depends on the risk and on the value of the events you’re insuring. For example, if you’re an inexperienced driver it’s more likely that you’ll have an accident, so your car insurance will cost more – and it’ll be even more if you drive an expensive car, because repairs will probably cost more.</li>
                <li>Make a claim. If something happens that’s covered by the policy, you can claim on your insurance. You tell the insurance company what happened, they check that it’s covered under your policy, and if the claim meets with what you’re protected against then they pay you as agreed.</li>

